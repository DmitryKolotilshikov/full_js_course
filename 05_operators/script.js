let x;
x = 2 ** 2; // 4 (возводит в степень)
x = 2 ** 3; // 8

x = 1;
// x = x + 1; // то же, что и x++
x++; // то же, что и x = x + 1
// x = x - 1; // то же, что и x--
x--; // то же, что и x = x - 1


// Операторы присваивания
let d = 10;

d += 1; // d = d + 1
d -= 1 // d = d - 1
d *= 1; // d = d * 1
d /= 1; // d = d / 1
d %= 2; // d = d % 2
d = d ** 2; // d = d ** 2
// -----------------------------------

// Операторы сравнения
// >, <, >=, <=
// ==, ===, !=, !== (также более подробно в 8 уроке)

console.log(1 == 1);
console.log(1 === 1);

console.log(1 == '1');
console.log(1 === '1');

console.log(1 != '1');
console.log(1 !== '1');
// -----------------------------------

